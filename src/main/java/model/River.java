package model;

public class River {
    private int gold;
    private int food;
    private int production;
    private int movement;
    private int x1;
    private int x2;
    private int y1;
    private int y2;

    public int getGold () {
        return gold;
    }

    public int getFood () {
        return food;
    }

    public int getProduction () {
        return production;
    }

    public int getMovement () {
        return movement;
    }

    public int getX1 () {
        return x1;
    }

    public int getX2 () {
        return x2;
    }

    public int getY1 () {
        return y1;
    }

    public int getY2 () {
        return y2;
    }

    public void setGold (int gold) {
        this.gold = gold;
    }

    public void setFood (int food) {
        this.food = food;
    }

    public void setProduction (int production) {
        this.production = production;
    }

    public void setMovement (int movement) {
        this.movement = movement;
    }

    public void setX1 (int x1) {
        this.x1 = x1;
    }

    public void setX2 (int x2) {
        this.x2 = x2;
    }

    public void setY1 (int y1) {
        this.y1 = y1;
    }

    public void setY2 (int y2) {
        this.y2 = y2;
    }
}
